// A Heap is a special Tree-based data structure in which the tree is a complete
// binary tree.

// ============================================================

// Types of Heap Data Structure

// 1. Max-Heap:
// In a Max-Heap the key present at the root node must be greatest among the
// keys present at all of it’s children. The same property must be recursively
// true for all sub-trees in that Binary Tree.
//
// 2. Min-Heap:
// In a Min-Heap the key present at the root node must be minimum among the keys
// present at all of it’s children. The same property must be recursively true
// for all sub-trees in that Binary Tree.

// ============================================================

// Operations of Heap Data Structure:

// 1. Heapify: a process of creating a heap from an array.

// 2. Insertion: process to insert an element in existing heap time complexity
// O(log N).

// 3. Deletion: deleting the top element of the heap or the highest priority
// element, and then organizing the heap and returning the element with time
// complexity O(log N).

// 4. Peek: to check or find the most prior element in the heap, (max or min
// element for max and min heap).

// ============================================================
